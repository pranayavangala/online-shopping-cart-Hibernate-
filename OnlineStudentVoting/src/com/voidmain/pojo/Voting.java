package com.voidmain.pojo;

public class Voting {

	private int id;
	private String votedBy;
	private String votedTo;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getVotedBy() {
		return votedBy;
	}
	public void setVotedBy(String votedBy) {
		this.votedBy = votedBy;
	}
	public String getVotedTo() {
		return votedTo;
	}
	public void setVotedTo(String votedTo) {
		this.votedTo = votedTo;
	}
}
